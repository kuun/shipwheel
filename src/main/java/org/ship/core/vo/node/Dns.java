public class Dns {
    private String dns;

    public String getDns() {
        return dns;
    }

    public void setDns(String dns) {
        this.dns = dns;
    }

    @Override
    public String toString() {
        return "Dns{" +
                "dns='" + dns + '\'' +
                '}';
    }
}
